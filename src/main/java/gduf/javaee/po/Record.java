package gduf.javaee.po;

public class Record {
    private String reid;
    private String cid;
    private String retype;
    private float reamount;
    private String retime;
    private String re_place;

    public String getReid() {
        return reid;
    }

    public void setReid(String reid) {
        this.reid = reid;
    }

    public String getCid() {
        return cid;
    }

    public void setCid(String cid) {
        this.cid = cid;
    }

    public String getRetype() {
        return retype;
    }

    public void setRetype(String retype) {
        this.retype = retype;
    }

    public float getReamount() {
        return reamount;
    }

    public void setReamount(float reamount) {
        this.reamount = reamount;
    }

    public String getRetime() {
        return retime;
    }

    public void setRetime(String retime) {
        this.retime = retime;
    }

    public String getRe_place() {
        return re_place;
    }

    public void setRe_place(String re_place) {
        this.re_place = re_place;
    }
}
